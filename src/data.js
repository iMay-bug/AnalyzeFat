export const getCurrentRankInfo = (xp = 0) => {
    let currentRankIndex = 0;
    for (let i = ranks.length - 1; i >= 0; i--) {
        if (xp >= ranks[i].threshold) {
            currentRankIndex = i;
            break;
        }
    }
    const currentRank = ranks[currentRankIndex];
    const nextRank = ranks[currentRankIndex + 1] || null;
    
    let progressPercentage = 100;
    let xpToNext = 0;
    if (nextRank) {
        const range = nextRank.threshold - currentRank.threshold;
        const currentProgress = xp - currentRank.threshold;
        progressPercentage = Math.min(100, Math.max(0, Math.floor((currentProgress / range) * 100)));
        xpToNext = nextRank.threshold - xp;
    }
    
    return {
        currentRank,
        nextRank,
        currentRankIndex,
        progressPercentage,
        xpToNext
    };
};

// HELPER PARA EXTRAIR DATA DO FEED
const getItemDate = (item) => {
    if (!item) return null;
    if (item.isoDate) return new Date(item.isoDate);
    if (item.id && !isNaN(parseInt(item.id.toString().substring(0, 13)))) {
        const ts = parseInt(item.id.toString().substring(0, 13));
        if (ts > 1600000000000 && ts < 2500000000000) return new Date(ts);
    }
    return new Date();
};

// FORMATAR DIA COMO YYYY-MM-DD LOCAMENTE
const getLocalDayStr = (date) => {
    if (!date || isNaN(date.getTime())) return '';
    const y = date.getFullYear();
    const m = String(date.getMonth() + 1).padStart(2, '0');
    const d = String(date.getDate()).padStart(2, '0');
    return `${y}-${m}-${d}`;
};

// CÁLCULO DE CONSISTÊNCIA (STREAK E GRADE DE 30 DIAS TIPO GITHUB/APPLE)
export const getConsistencyStats = (feed = []) => {
    const validItems = feed.filter(i => typeof i !== 'string');
    const dayCounts = {};
    const trainedDaysSet = new Set();

    validItems.forEach(item => {
        const d = getItemDate(item);
        if (d && !isNaN(d.getTime())) {
            const dayStr = getLocalDayStr(d);
            dayCounts[dayStr] = (dayCounts[dayStr] || 0) + 1;
            trainedDaysSet.add(dayStr);
        }
    });

    const today = new Date();
    const todayStr = getLocalDayStr(today);
    
    const yesterday = new Date(today);
    yesterday.setDate(today.getDate() - 1);
    const yesterdayStr = getLocalDayStr(yesterday);

    // CÁLCULO DO STREAK DE DIAS SEGUIDOS
    let streak = 0;
    let checkDate = new Date(today);
    let checkStr = getLocalDayStr(checkDate);

    // Se hoje não treinou ainda, verifica se ontem treinou para não zerar o streak logo de manhã
    if (!trainedDaysSet.has(todayStr) && trainedDaysSet.has(yesterdayStr)) {
        checkDate = yesterday;
        checkStr = yesterdayStr;
    }

    while (trainedDaysSet.has(checkStr)) {
        streak++;
        checkDate.setDate(checkDate.getDate() - 1);
        checkStr = getLocalDayStr(checkDate);
    }

    // TREINOS NA ÚLTIMA SEMANA (7 DIAS)
    let thisWeekCount = 0;
    for (let i = 0; i < 7; i++) {
        const d = new Date(today);
        d.setDate(today.getDate() - i);
        if (trainedDaysSet.has(getLocalDayStr(d))) {
            thisWeekCount++;
        }
    }

    // GRADE DOS ÚLTIMOS 60 DIAS (HEATMAP PROFISSIONAL STYLE WHOOP/GITHUB)
    const last60Days = [];
    for (let i = 59; i >= 0; i--) {
        const d = new Date(today);
        d.setDate(today.getDate() - i);
        const str = getLocalDayStr(d);
        const dayLabel = `${String(d.getDate()).padStart(2, '0')}/${String(d.getMonth() + 1).padStart(2, '0')}`;
        const count = dayCounts[str] || 0;
        last60Days.push({
            dateStr: str,
            dayLabel,
            count,
            hasWorkout: count > 0
        });
    }

    const last30Days = last60Days.slice(30);

    return {
        streak,
        thisWeekCount,
        last30Days,
        last60Days,
        totalActiveDays: trainedDaysSet.size
    };
};

